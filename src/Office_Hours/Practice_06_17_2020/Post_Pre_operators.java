package Office_Hours.Practice_06_17_2020;

public class Post_Pre_operators {

    public static void main(String[] args) {

        int a = 300;

        System.out.println(++a); // a = 301
        System.out.println(++a); // a = 302


        int i = 50;
        System.out.println(--i); // i = 49
        System.out.println(--i); // i = 48

        System.out.println("***********");


        int c = 300;

        System.out.println(c++); // a = 300
        System.out.println(c++); // a = 301


        int d = 50;
        System.out.println(d--); // i = 50
        System.out.println(d--); // i = 49




    }
}
