package day08_Logical_Operators;

public class liter_to_gallon {

    /*write a java program that converts liters to gallons
	 					1 gallon = 3.785 liters
	 					1 liter = 1/3.785


     */
    public static void main(String[] args) {

        double liter = 75;
        double gallon = liter / 3.785;

        System.out.println(liter+ " liters equal to "+(int)gallon+ " gallons.");



    }



}
