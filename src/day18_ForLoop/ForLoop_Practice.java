package day18_ForLoop;

public class ForLoop_Practice {
    public static void main(String[] args) {



        for (int i = 1; i <= 5; i++){
            System.out.println("Cybertek Batch 20");
        }
        System.out.println("==============");
       for (int i = 1; i <= 10; ++i){
           System.out.print(i+" ");
       }
        System.out.println("==============");

        for (int i = 10; i >= 1; --i) {
            System.out.print(i+" ");
        }
        System.out.println("==============");
        for (int i = 1; i <= 100; ++i) {
            System.out.print(i + " ");
        }
        System.out.println("==============");
        for (int i = 1; i <= 10; ++i) {
            System.out.println(i + " ");
        }
        System.out.println("==============");
        for (int i = 100; i >= 1; --i) {
            System.out.print(i + " ");
        }

        System.out.println("==============");
        for (int i = 10; i <= 100; i++) {
            System.out.print(i + " ");
        }





    }
}
