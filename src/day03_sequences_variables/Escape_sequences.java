package day03_sequences_variables;

public class Escape_sequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone! \nMy name is Cybertek :)");
        System.out.println("\tI'm in Fishers.\n");
        System.out.println("\n\n\t\t\tI love programming!");
        System.out.println("\\ "); //one backslash
        System.out.println("\\\\");//two backslash
        System.out.println("/");//one front slash
        System.out.println("//");//two front slash

        System.out.println("My name is \'Sevda\'.");//'Sevda'
        System.out.println("My name is 'Sevda'.");//'Sevda'

        System.out.println("My favourite TV series:\"LOST\" ");
        System.out.println();


    }



}
