package day07Unary_shorthand;

public class Unary {
    public static void main(String[] args) {

        System.out.println(-1 + -10);// -1 - 10 = -11
        System.out.println(-1 - -10); // -1 + 10 = 9

     // --:
        int a = 20;
        //a = a - 1;
        --a;

        System.out.println(a);

      // ++:
      int b = 29;
      ++b;
        System.out.println(b);

        System.out.println("======");







    }
}
